package string;
import java.util.*;
public class stringrev {
  public static void main(String arg[])
  {
	  String user="milan";
	  
	  char[] charArray=user.toCharArray();
	  for(char c : charArray)
	  {
		  System.out.println(c);
	  }
	  for(int i=user.length();i>0;i--)
	  {
		  System.out.println();
	  }
	  
		  
	 
  } 
}
